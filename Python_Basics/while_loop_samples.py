def while_loop():
    a = int(input("Enter a number to print the list up to that number: "))
    i = 0
    while (i < (a+1)):
        print(i, end= ' ')
        i += 1
while_loop()


def squares_of_number():
    b = int(input("\nEnter a number to print squares up to that number: "))
    j = 0
    while(j <  (b+1)):
        print(j*j, end = ' ')
        j += 1
squares_of_number()


