
'''
1. Python Solution (Addition and Subtraction Assignment)
Task: Start with balance = 1000. Apply a $50 deposit using +=, and a $15 fee using -=.
'''
balance = 1000
print(f"Bank Account Balance: ${balance}")

# Apply $50 deposit using Addition Assignment
balance += 50

# Apply $15 fee using Subtraction Assignment
balance -= 15
print(f"Final Balance Amount: ${balance}")