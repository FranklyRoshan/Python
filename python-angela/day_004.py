def rock_paper_scissors():
    import random

    rock = '''
        _______
    ---'   ____)
            (_____)
            (_____)
            (____)
    ---.__(___)
    '''

    paper = '''
        _______
    ---'   ____)____
              ______)
                _______)
                _______)
    ---.__________)
    '''

    scissors = '''
        _______
    ---'    ____)____
                 ______)
            __________)
         (____)
    ---.__(___)
    '''

    rps = [rock, paper, scissors]
    user = int(input("What do you choose? Type 0 for Rock, 1 for Paper, 2 for Scissors: \n"))

    if user < 0 or user > 2:
        print("Invalid choice")
    else:
        computer = random.randint(0, 2)
        print("You chose: ")
        print(rps[user])
        print("Computer chose: ")
        print(rps[computer])

        if user == computer:
            print("It's a Draw")
        elif (user == 0 and computer == 2) or \
            (user == 1 and computer == 0) or \
            (user == 2 and computer == 1):
            print("You win!")
        else:
            print("You lose")
rock_paper_scissors()


