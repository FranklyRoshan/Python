def banker_roulette():
    import random
    friends = ["Alice", "Bob", "Charlie", "David", "Emanuel"]

    # random.randint
    list_len = len(friends)
    rand = random.randint(0,list_len)
    print(friends[rand])

    # random.choice
    print(random.choice(friends))
banker_roulette()

