import random

def rock_paper_scissors():

    rps = [
        '''
        _______
    ---'   ____)
          (_____)
          (_____)
          (____)
    ---.__(___)
        ''',

        '''
        _______
    ---'   ____)____
              ______)
              _______)
             _______)
    ---.__________)
        ''',

        '''
        _______
    ---'   ____)____
              ______)
           __________)
          (____)
    ---.__(___)
        '''
    ]

    user = int(input("Type 0 for Rock, 1 for Paper, 2 for Scissors: "))

    if user not in [0, 1, 2]:
        print("Invalid choice")
        return

    computer = random.choice([0, 1, 2])

    print("You chose:")
    print(rps[user])

    print("Computer chose:")
    print(rps[computer])

    if user == computer:
        print("Draw")
    elif (user - computer) % 3 == 1:
        print("You Win!")
    else:
        print("You Lose!")


rock_paper_scissors()